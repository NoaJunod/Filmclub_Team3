import java.util.Date;

/**
 *
 * @author Laura LÃ¼thi
 */
public class Championship {

    private Date date;
    private String place;
    private String title;

    public Championship(Date date, String place, String title) {
        this.date = date;
        this.place = place;
        this.title = title;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

   

    public String getPlace() {
        return place;
    }

    public void setPlace(String place) {
        this.place = place;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

}
